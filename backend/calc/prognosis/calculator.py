    today = _dt.date.today()

    # -----------------------------
    # –†–ò–¢–ú –ù–ê –ì–û–î
    # –§–æ—Ä–º—É–ª–∞: –¥–µ–Ω—å —Ä–æ–∂–¥–µ–Ω–∏—è + –º–µ—Å—è—Ü —Ä–æ–∂–¥–µ–Ω–∏—è + —Å—É–º–º–∞ —Ü–∏—Ñ—Ä –∏–Ω—Ç–µ—Ä–µ—Å—É—é—â–µ–≥–æ –≥–æ–¥–∞
    # -----------------------------
    year_digits_sum = _sum_digits(today.year)
    year_raw = bdate.day + bdate.month + year_digits_sum
