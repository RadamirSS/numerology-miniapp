    # –§–æ—Ä–º—É–ª–∞: —Ä–∏—Ç–º –º–µ—Å—è—Ü–∞ + —á–∏—Å–ª–æ —Ç–µ–∫—É—â–µ–≥–æ –¥–Ω—è
    # -----------------------------
